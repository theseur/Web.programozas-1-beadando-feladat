    amit ma gondolunk küldetésünkrõl, és a szegények istápolása is bizonyosan más tartalommal bírt akkoriban.
</p>
<p style="font-size:20px;" class="p-2">Ettõl azonban jelmondatunk nem vált idejét múlttá, hiszen napjainkban ugyanolyan fontos a hit védelme, amelyet ma
